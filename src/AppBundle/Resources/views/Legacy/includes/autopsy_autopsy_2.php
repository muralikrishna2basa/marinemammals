<h3>Necropsy</h3>

<p>The aims of necropsies are to provide information about (1) the cause of death,; (2) identification of lesions and their potential origin and therewith the health status of and the mains threats to a population and (3) collection of samples.   Other investigations from samples collected during necropsy can provide data on biology (reproductive status, life history,...), physiology, anthropogenic pollutant burdens of the marine mammals.</p>

<p>It was always better to perform the necropsy as soon as possible after the standings but, in practise, that is not always possible. Indeed, in case of mass strandings or epizootics (morbillivirus), there are too many animals and other solutions have to be found. To understand exactly what is happening, it is recommended to perform the necropsy of almost animals, if not all. The best solution is to frozen (-20&deg;C)   animals (only for small cetaceans and pinnipeds) that can be not investigate directly.</p>

<p>As all informations (pathology, toxicology, life history,...) to be gained by the necropsy is valuable by adding to our knowledge of marine mammals (very limited knowledge for most large whale species), major efforts should be done to collect as much samples and data as possible.   Considering the effort necessary, and to achieve a maximum of results by sensible data and sample collection, a list of required samples (see annex) and processing laboratories (see annex) should be available beforehand.   Such basic list is certainely expandable for futher or more detailed research and should be updated regularly.   It should not be forgotten that scientific information is exploitable only when carefully documented.   While there are probably as many ways to dissect a cetacean as there are pathologists, the aims of this report is to propose a standard procedure for the management of stranding and for the necropsy of marine mammals and tissues sampling for an intervention level 3 (see below).   Protocol could be used as guideline for bettering post-mortem investigations on those species.</p>

<p>The methodology presented below is organized in two parts: (1) general considerations for marine mammals necropsy (2) particularities for small cetaceans, pinnipeds and large cetaceans. </p>

